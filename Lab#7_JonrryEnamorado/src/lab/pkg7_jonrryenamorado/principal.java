    public void listar_todo(piezas p_raiz, DefaultMutableTreeNode nodo) {
        try {
            
            ArrayList<piezas> l3 = p_raiz.getPiezas();
             
            for (piezas temp : l3) {
                if (temp.getPiezas().isEmpty()) {
                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(temp.getNombre());
                    nodo.add(n);
                } else {
                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(temp.getNombre());
                    nodo.add(n);
                    listar_todo(temp, n);

                }
            }
        } catch (Exception e) {
        }
    }
    
    
